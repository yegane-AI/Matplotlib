
import numpy as np
import matplotlib.pyplot as plt
#import matplotlib.pylab as pl
x = np.arange(0, 2*np.pi, 0.1);
#x = np.linspace(0, 2*np.pi, 64)
y = np.sin(x)

#plt.figure()
plt.plot(x,y)

n = 20
rangi = plt.cm.jet(np.linspace(0,1,n))

for i in range(n):
    plt.plot(x, i*y, color=rangi[i])

plt.show()
